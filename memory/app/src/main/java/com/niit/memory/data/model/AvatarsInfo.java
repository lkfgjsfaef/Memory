package com.niit.memory.data.model;

public class AvatarsInfo {
    private String hisNickname;
    private String herNickname;
    private String hisAvatarUrl;
    private String herAvatarUrl;

    public String getHisNickname() { return hisNickname; }
    public void setHisNickname(String hisNickname) { this.hisNickname = hisNickname; }
    public String getHerNickname() { return herNickname; }
    public void setHerNickname(String herNickname) { this.herNickname = herNickname; }
    public String getHisAvatarUrl() { return hisAvatarUrl; }
    public void setHisAvatarUrl(String hisAvatarUrl) { this.hisAvatarUrl = hisAvatarUrl; }
    public String getHerAvatarUrl() { return herAvatarUrl; }
    public void setHerAvatarUrl(String herAvatarUrl) { this.herAvatarUrl = herAvatarUrl; }
}
